package com.turnon.web.model;

import org.joda.time.Hours;
import org.joda.time.Minutes;

public class HourMinute {
	private Hours hour;
	private Minutes minute;
	public Hours getHour() {
		return hour;
	}
	public void setHour(Hours hour) {
		this.hour = hour;
	}
	public Minutes getMinute() {
		return minute;
	}
	public void setMinute(Minutes minute) {
		this.minute = minute;
	}
	
	

}
